package Entity;

/**
 *
 * @author Admin
 */
public class Deposit {
    private int id;
    private int userid;
    private String username;
    private int money;
    private int status;

    public Deposit() {
    }

    public Deposit(int id, int userid, String username, int money, int status) {
        this.id = id;
        this.userid = userid;
        this.username = username;
        this.money = money;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

}
